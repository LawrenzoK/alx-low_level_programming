#include <stdio.h>

/**
 * printPutchar - prints a string using putchar
 */
void printPutchar(void)
{
	putchar('_');
	putchar('p');
	putchar('u');
	putchar('t');
	putchar('c');
	putchar('h');
	putchar('a');
	putchar('r');
	putchar('\n');
}

/**
 * main - Entry point
 *
 * Return: Always 0 (success)
 */
int main(void)
{
	printPutchar();

	return (0);
}
